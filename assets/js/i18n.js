/* ========== Initialize i18n ========== */

let currentLang = localStorage.getItem('nawah-lang') || 'en';

function detectLanguage() {
  const saved = localStorage.getItem('nawah-lang');
  if (saved) return saved;
  const browserLang = navigator.language || navigator.userLanguage;
  if (browserLang.startsWith('ar')) return 'ar';
  return 'en';
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('nawah-lang', lang);

  const html = document.documentElement;
  if (lang === 'ar') {
    html.setAttribute('dir', 'rtl');
    html.setAttribute('lang', 'ar');
  } else {
    html.setAttribute('dir', 'ltr');
    html.setAttribute('lang', 'en');
  }

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = translations[lang]?.[key];
    if (translation) {
      el.textContent = translation;
    }
  });

  // Update meta tags
  const metaTitle = document.querySelector('meta[property="og:title"]');
  const metaDesc = document.querySelector('meta[property="og:description"]');
  const metaDescName = document.querySelector('meta[name="description"]');

  const titleKey = html.getAttribute('data-meta-title');
  const descKey = html.getAttribute('data-meta-desc');

  if (titleKey && translations[lang]?.[titleKey]) {
    document.title = translations[lang][titleKey];
    if (metaTitle) metaTitle.setAttribute('content', translations[lang][titleKey]);
  }
  if (descKey && translations[lang]?.[descKey]) {
    if (metaDesc) metaDesc.setAttribute('content', translations[lang][descKey]);
    if (metaDescName) metaDescName.setAttribute('content', translations[lang][descKey]);
  }

  // Dispatch event for other scripts
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

function toggleLanguage() {
  const newLang = currentLang === 'ar' ? 'en' : 'ar';
  setLanguage(newLang);

  // Update toggle button text
  const langToggle = document.getElementById('lang-toggle');
  if (langToggle) {
    langToggle.textContent = newLang === 'ar' ? 'EN' : 'AR';
  }
}

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  const detected = detectLanguage();
  setLanguage(detected);

  const langToggle = document.getElementById('lang-toggle');
  if (langToggle) {
    langToggle.textContent = detected === 'ar' ? 'EN' : 'AR';
    langToggle.addEventListener('click', toggleLanguage);
  }
});
